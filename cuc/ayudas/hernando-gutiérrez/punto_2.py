# vector inicial
vector = [1, 2, 3, 4, 5]

# vector con números al cuadrado
vector_cuadrados = []
for i in vector:
    vector_cuadrados.append(i**2)

# vector con números al cubo
vector_cubos = []
for i in vector:
    vector_cubos.append(i**3)

# vector con números a la cuatro
vector_cuatro = []
for i in vector:
    vector_cuatro.append(i**4)

# resultados
print("Vector inicial:", vector)
print("Vector al cuadrado:", vector_cuadrados)
print("Vector al cubo:", vector_cubos)
print("Vector a la cuatro:", vector_cuatro)
