# Imprimir 10 veces un nombre (while)
nombre = input("Ingrese un nombre: ")

# Inicializar variable de control
i = 0
while i < 10: # Condición del ciclo
    # Procedimiento
    print(nombre)
    # Modificar variable de control
    i = i + 1

# Imprimir 10 veces un nombre (for)
nombre = input("Ingrese un nombre: ")

# Indicar número repeticiones
for i in range(10): 
    # Procedimiento
    print(nombre) 