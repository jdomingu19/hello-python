print("\nPor favor ingrese 3 numeros enteros positivos o negativos: ")
n1=int(input("Ingrese su primer numero: "))
n2=int(input("Ingrese su segundo numero: "))
n3=int(input("Ingrese su tercer numero: "))
#condicionales positivo y negativo
if n1>=0:
    print(f"El numero {n1} es positivo")

else:
    print(f"El numero {n1} es negativo")

if n2>=0:
    print(f"El numero {n2} es positivo")

else:
    print(f"El numero {n2} es negativo")

if n3>=0:
    print(f"El numero {n3} es positivo\n")

else:
    print(f"El numero {n3} es negativo\n")